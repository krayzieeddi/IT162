 <p><i>“The way you display your content can say a lot about you or your company. Having nice content and knowing how to show your content properly in a clean way that your user can understand is really important.”</i> (Maldonado)</p>

<p>Only just a few years ago web developers were using float, inline-blocking, and positioning CSS tricks to mad their layouts work.</p>

 <p>Now with flexbox, we now have a new layout tool for developers to use. Flexbox is also very using in maintenance and refactoring for its one-dimensional layout system can create vertical and horizontal compositions without using all the old tricks.</p>

 <p>When considering when to use flexbox is when your site is only going to have a small UI design when you need to align elements within a container, and to layout, your content before you have a final design in mind. You have the ability to build your entire site just by using flexbox, but ideally when your site’s UI design is small or just for mobile. In combination with using a CSS grid, you would have many options and flexibility with your site.</p>

 <p>For bigger more complex sites you would want to use a CSS grid and then use flexbox within the containers in the grid to layout more content. But overall just remember that grid is for layout while flexbox is for alignment of content.</p>

 <p><i>“Flexbox is very well supported across modern browsers, however, there are a few issues that you might run into.”</i>(Backwards Compatibility of flexbox)</p>

 <p>Even though flexbox is somewhat new, support for the new browser is really good. But as you go further into older websites there will be conflicts as bugs sprout up for older versions of websites. Each bug is currently catalog on <a href="https://github.com/philipwalton/flexbugs">github.com/philipwalton/flexbugs</a> does have a workaround.</p>
    

