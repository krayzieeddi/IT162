 <p><i>“In 2016, for the first time ever, mobile web usage surpassed desktop usage, and believe it or not, businesses are still adapting to that change!”</i> (Labus)</p>
 
 <p>Mobile dedicated sites are only for mobile devices as they contain features that are only deemed suitable on mobile and no other devices. As when mobile sites are viewed on other screen resolutions other than mobile the arrangement of the site doesn’t change, so do the features. They often are a separate site entirely from their full site. Usually, the features on mobile are just a portion of all the other remaining features of the main site. </p>

<p><i>“One of the most important things to keep in mind is the fact that Google now ranks mobile sites above desktop sites for SEO ranking”</i> (Labus)</p>

<p>Even though mobile usage is surpassing desktop users, the UI on the mobile is still limited. As mobile functionality isn’t as user-friendly as it is.</p>

<p><i>“Responsive design is a development technique that detects the client type and dynamically adjusts the layout of a site according to the size of the screen on which it is displayed”</i> (Budiu)</p>

<p>For responsive design ideally, instead of cutting out content in mobile-only sites, the site readjusts all of its data to fit the screen. Though in actuality just 100% of the content is rearranged while some other features are cut that don’t see much use on mobile.</p>

<p>In summary, mobile design is designing solely for mobile screens, while responsive is designing for all devices with different screens in mind. Even though the mobile market is growing larger and larger every year, using a responsive design would be best. As responsive takes care of all kinds of users. Mobile may be the largest customer base out there, you still have the desktop users that still make up a sizable chunk of the market and you wouldn’t want to miss out on them. 
</p>
    

