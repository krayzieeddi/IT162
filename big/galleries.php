<p><i>“jQuery sliders offer a simple, elegant way to display content without giving up valuable real estate on your web page. They have skyrocketed in popularity and can be used in a variety of ways.”</i> (Ziegler)</p>

<p>Before galleries images only could be displayed as images on their own. Now with galleries and jQuery sliders, you can display numerous amounts of images without taking up real estate.</p>
    
<p>There are many plugins out there for jQuery sliders and they all have their ups and downs. Depending on what type of content you want to display there is a premium slider plugin out there for your needs. With all kinds of data, you can display all different types of transitions, animations, and content. There are plenty of tutorials, posts, and marketplaces of all kinds of sliders out there. You just have to go searching.</p>

<p>	If you can’t find exactly what you’re looking for you always design your own sliders. Using tools like Firebug can let you insect other sliders so you can dissect the work. Not to copy it but to get the idea of how to manipulate sliders in general. </p>

<p>Popular sliders right would be the standard photo slider where it’s the easiest for beginners but lack the functionality of supporting a wide variety of the type of content you can put in the slider. You then have the mixed content sliders which obviously support different types of content but are more complex in implementing the slider into your site.</p>

