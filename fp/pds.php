            <p class="question">School Counseling Services</p>
            <p>
                School Counseling is based on a trusting relationship between 
                the school counselor and student. Information shared with me is 
                considered confidential and will not be shared with others, 
                except in certain situations where it is my ethical 
                responsibility to limit confidentiality and share relevant 
                information. Students and families will always be notified 
                of the limitations of confidentiality and when I must share 
                relevant information with others. The situations are when:
            </p>

            <ul>
                <li>A person is believed to be a danger to themself.</li>
                <li>A person is believed to be in danger of hurting someone else.</li>
                <li>A person is suspected of being abused, neglected, or abandoned.</li>
                <li>The school counselor's records are subpoenaed by court order.</li>
            </ul>

