            <p class="question">Philosophy and Approach</p>
            <p>
                I believe that the role of a school counselor is to support 
                students and facilitate growth in the areas of social-emotional 
                health, academics, as well as college and career readiness. I 
                practice from an Integrative Therapy approach which allows me 
                to pull from and work with strategies and techniques from various 
                counseling theories and therapies. As a school counselor, I have 
                largely practiced from a strengths-based and solution-focused 
                approach as I have found these are beneficial to students and an 
                efficient use of our time together. However, what works well for 
                one student, may not work well with every student which is why I 
                understand the importance of working one-on-one with students.
            </p>

